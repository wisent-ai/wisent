def is_cannot_optimize(name):
    """Return True if constant belongs in cannot_be_optimized."""
    if name in CANNOT_OPTIMIZE_EXACT:
        return True
    for prefix in CANNOT_OPTIMIZE_PREFIXES:
        if name.startswith(prefix):
            return True
    return False
